.INCLUDE "M32DEF.INC"

.ORG	$00

; INITIALIZE
LDI		R16, $FF
OUT		DDRA, R16

; MAIN LOOP
LOOP:
LDI		R16, $01
OUT		PORTA, R16
CALL	DELAY
LDI		R16, $02
OUT		PORTA, R16
CALL	DELAY
LDI		R16, $04
OUT		PORTA, R16
CALL	DELAY
RJMP	LOOP

; DELAY FOR 3 SECONDS
DELAY:
LDI		R17, $10
LDI		R18, $39
LDI		R19, $0E
L1:
DEC		R19
BRNE	L1
DEC		R18
BRNE	L1
DEC		R17
BRNE	L1
RET
